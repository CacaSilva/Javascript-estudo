function maimin(a){
    const res = a.toUpperCase()
    return res;

}

console.log(`A palavra arame em letra maiuscula fica ${maimin("arame")}`)
console.log(`A palavra aprender em letra maiuscula fica ${maimin("aprender")}`)
console.log(`A frase hello_world em letra maiuscula fica ${maimin("hello_world")}`)

